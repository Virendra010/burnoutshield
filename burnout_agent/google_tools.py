# ── Combined Exhaustion Analysis ──────────────────────────────────────────────

def analyze_exhaustion(
    calendar_events: list[dict], gmail_signals: list[dict],
    tasks: list[dict], meetings: list[dict], deadlines: list[dict]
) -> dict:
    cal_pressure = analyze_calendar_pressure(calendar_events)
    email_pressure = analyze_email_pressure(gmail_signals)

    score = 0
    stressors = []

    meeting_hours = cal_pressure.get("total_meeting_hours", 0)
    if meeting_hours >= 6: score += 30; stressors.append(f"Extreme meeting load: {meeting_hours}h")
    elif meeting_hours >= 4: score += 20; stressors.append(f"Heavy meeting load: {meeting_hours}h")
    elif meeting_hours >= 2: score += 10

    b2b = cal_pressure.get("back_to_back_count", 0)
    if b2b >= 3: score += 15; stressors.append(f"{b2b} back-to-back meetings without breaks")
    elif b2b >= 1: score += 8; stressors.append(f"{b2b} back-to-back meeting(s)")

    meet_count = cal_pressure.get("google_meet_count", 0)
    if meet_count >= 4: score += 10; stressors.append(f"{meet_count} video calls (fatigue risk)")

    urgent_emails = email_pressure.get("total_urgent_emails", 0)
    if urgent_emails >= 5: score += 15; stressors.append(f"{urgent_emails} urgent emails")
    elif urgent_emails >= 3: score += 10

    task_count = len(tasks)
    if task_count >= 12: score += 15; stressors.append("Too many tasks scheduled")
    
    critical_deadlines = [d for d in deadlines if d.get("severity") == "critical" or d.get("due") == "today"]
    if len(critical_deadlines) >= 2: score += 10; stressors.append("Multiple critical deadlines today")

    score = min(score, 100)
    if score >= 70: level = "SEVERE"
    elif score >= 45: level = "HIGH"
    elif score >= 25: level = "MODERATE"
    else: level = "LOW"

    return {
        "exhaustion_score": score,
        "exhaustion_level": level,
        "calendar_pressure": cal_pressure,
        "email_pressure": email_pressure,
        "key_stressors": stressors,
        "recommendations": [],
        "signal_summary": {
            "calendar_events": len(calendar_events),
            "urgent_emails": urgent_emails,
            "tasks": task_count,
        }
    }
