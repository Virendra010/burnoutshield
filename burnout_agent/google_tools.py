# ── Gmail Signals ─────────────────────────────────────────────────────────────

_URGENCY_KEYWORDS = [
    "urgent", "asap", "immediately", "deadline", "overdue",
    "action required", "follow up", "reminder", "time-sensitive",
    "critical", "escalation", "blocked", "blocker", "eod", "end of day",
]

_MEETING_KEYWORDS = [
    "meeting", "invite", "calendar", "reschedule", "agenda",
    "standup", "sync", "review", "demo", "presentation",
]


def get_gmail_signals(max_results: int = 30) -> list[dict]:
    if not GMAIL_ENABLED:
        return _mock_gmail_signals()

    creds = _get_google_creds()
    if not creds:
        return _mock_gmail_signals()

    try:
        from googleapiclient.discovery import build

        service = build("gmail", "v1", credentials=creds, cache_discovery=False)

        query = "is:unread newer_than:1d"
        results = (
            service.users()
            .messages()
            .list(userId="me", q=query, maxResults=max_results)
            .execute()
        )

        messages = results.get("messages", [])
        signals  = []

        for msg in messages:
            msg_data = (
                service.users()
                .messages()
                .get(userId="me", id=msg["id"], format="metadata",
                     metadataHeaders=["Subject", "From", "Date"])
                .execute()
            )

            headers = {h["name"]: h["value"] for h in msg_data.get("payload", {}).get("headers", [])}
            subject = headers.get("Subject", "")
            snippet = msg_data.get("snippet", "")
            sender  = headers.get("From", "")
            date    = headers.get("Date", "")

            combined_text = (subject + " " + snippet).lower()

            found_urgency = [kw for kw in _URGENCY_KEYWORDS if kw in combined_text]
            urgency_score = len(found_urgency) * 2

            found_meeting = [kw for kw in _MEETING_KEYWORDS if kw in combined_text]
            is_meeting_related = len(found_meeting) > 0

            if found_urgency or is_meeting_related:
                signals.append({
                    "subject": subject,
                    "snippet": snippet[:150],
                    "from": sender,
                    "date": date,
                    "urgency_keywords_found": found_urgency,
                    "urgency_score": urgency_score,
                    "is_meeting_related": is_meeting_related,
                })

        logging.info(f"[Gmail] {len(signals)} signal(s)")
        return signals

    except Exception as e:
        logging.warning(f"[Gmail] API error: {e}")
        return _mock_gmail_signals()


def analyze_email_pressure(signals: list[dict]) -> dict:
    if not signals:
        return {
            "total_urgent_emails": 0, "total_meeting_emails": 0,
            "max_urgency_score": 0, "top_urgent_subjects": [],
            "email_pressure_level": "LOW",
        }

    urgent = [s for s in signals if s.get("urgency_score", 0) > 0]
    meeting_related = [s for s in signals if s.get("is_meeting_related", False)]

    sorted_by_urgency = sorted(urgent, key=lambda x: x.get("urgency_score", 0), reverse=True)
    top_subjects = [s["subject"] for s in sorted_by_urgency[:3]]
    max_score = sorted_by_urgency[0].get("urgency_score", 0) if sorted_by_urgency else 0

    total_urgent = len(urgent)
    if total_urgent >= 8 or max_score >= 8: pressure = "CRITICAL"
    elif total_urgent >= 5 or max_score >= 6: pressure = "HIGH"
    elif total_urgent >= 3 or max_score >= 4: pressure = "MEDIUM"
    else: pressure = "LOW"

    return {
        "total_urgent_emails": total_urgent, "total_meeting_emails": len(meeting_related),
        "max_urgency_score": max_score, "top_urgent_subjects": top_subjects,
        "email_pressure_level": pressure,
    }


