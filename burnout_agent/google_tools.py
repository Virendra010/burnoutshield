"""
google_tools.py — BurnoutShield Google Integrations (Enhanced Web OAuth)

Provides deep integration with Google Workspace:
  - get_calendar_events()       → Today's Google Calendar events + Google Meet detection
  - get_gmail_signals()         → Urgent email signal detection from Gmail
  - analyze_calendar_pressure() → Back-to-back, meeting hours, overload detection
  - analyze_exhaustion()        → Combined exhaustion scoring from all Google signals

Authentication:
  - Exclusively uses `token.json` generated by the Flask Web OAuth flow.
  - If `token.json` is missing or invalid, it gracefully falls back to realistic mock data.
"""

import os
import logging
import datetime
from pathlib import Path

from dotenv import load_dotenv

load_dotenv()

CALENDAR_ENABLED = os.getenv("GOOGLE_CALENDAR_ENABLED", "false").lower() == "true"
GMAIL_ENABLED    = os.getenv("GOOGLE_GMAIL_ENABLED",    "false").lower() == "true"

# Path resolution: credentials live in the project root
_PROJECT_ROOT    = Path(__file__).parent.parent
TOKEN_FILE       = _PROJECT_ROOT / "token.json"

SCOPES = [
    "https://www.googleapis.com/auth/calendar.readonly",
    "https://www.googleapis.com/auth/gmail.readonly",
]


# ── Authentication ────────────────────────────────────────────────────────────

def _get_google_creds():
    """
    Returns valid Google credentials from the token.json generated by the app's Web Flow.
    Automatically refreshes the token if expired.
    Returns None if unauthenticated.
    """
    if not TOKEN_FILE.exists():
        logging.info("[Google Auth] token.json not found — UI must login via Web OAuth. Using mock data.")
        return None

    try:
        from google.oauth2.credentials import Credentials
        from google.auth.transport.requests import Request

        creds = Credentials.from_authorized_user_file(str(TOKEN_FILE), SCOPES)

        if creds and creds.expired and creds.refresh_token:
            creds.refresh(Request())
            # Save the refreshed token
            with open(TOKEN_FILE, "w") as tok:
                tok.write(creds.to_json())

        if creds and creds.valid:
            # Optionally check if calendar/gmail access is actually granted
            return creds
            
        logging.warning("[Google Auth] token.json is invalid or expired with no refresh token.")
        return None

    except Exception as e:
        logging.warning(f"[Google Auth] Could not load or refresh token: {e}")
        return None


# ── Google Calendar ───────────────────────────────────────────────────────────

def get_calendar_events(max_results: int = 25) -> list[dict]:
    """
    Fetches today's Google Calendar events.
    Returns a list of dicts with keys: title, start, end, duration_min, etc.
    """
    if not CALENDAR_ENABLED:
        return _mock_calendar_events()

    creds = _get_google_creds()
    if not creds:
        return _mock_calendar_events()

    try:
        from googleapiclient.discovery import build

        service = build("calendar", "v3", credentials=creds, cache_discovery=False)

        now   = datetime.datetime.utcnow()
        start = now.replace(hour=0, minute=0, second=0, microsecond=0).isoformat() + "Z"
        end   = now.replace(hour=23, minute=59, second=59).isoformat() + "Z"

        events_result = (
            service.events()
            .list(
                calendarId="primary",
                timeMin=start,
                timeMax=end,
                maxResults=max_results,
                singleEvents=True,
                orderBy="startTime",
            )
            .execute()
        )

        raw_events = events_result.get("items", [])
        parsed = []

        for evt in raw_events:
            s = evt.get("start", {})
            e = evt.get("end", {})
            start_str = s.get("dateTime", s.get("date", ""))
            end_str   = e.get("dateTime", e.get("date", ""))
            is_all_day = "date" in s and "dateTime" not in s

            duration_min = 0
            if start_str and end_str and not is_all_day:
                try:
                    s_dt = datetime.datetime.fromisoformat(start_str.replace("Z", "+00:00"))
                    e_dt = datetime.datetime.fromisoformat(end_str.replace("Z", "+00:00"))
                    duration_min = int((e_dt - s_dt).total_seconds() / 60)
                except Exception:
                    pass

            # ── Google Meet / video conference detection ──
            meeting_link = ""
            has_google_meet = False
            location = evt.get("location", "")

            conf_data = evt.get("conferenceData", {})
            entry_points = conf_data.get("entryPoints", [])
            for ep in entry_points:
                if ep.get("entryPointType") == "video":
                    meeting_link = ep.get("uri", "")
                    break

            if not meeting_link and "hangoutLink" in evt:
                meeting_link = evt["hangoutLink"]
                has_google_meet = True
            elif meeting_link and "meet.google.com" in meeting_link:
                has_google_meet = True

            if not meeting_link and location.startswith("http"):
                meeting_link = location

            attendees = evt.get("attendees", [])

            parsed.append({
                "title":           evt.get("summary", "Untitled event"),
                "start":           start_str,
                "end":             end_str,
                "duration_min":    duration_min,
                "is_all_day":      is_all_day,
                "location":        location,
                "meeting_link":    meeting_link,
                "has_google_meet": has_google_meet,
                "attendee_count":  len(attendees),
                "is_recurring":    "recurringEventId" in evt,
                "status":          evt.get("status", "confirmed"),
                "organizer":       evt.get("organizer", {}).get("email", ""),
            })

        logging.info(f"[Calendar] Fetched {len(parsed)} event(s)")
        return parsed

    except Exception as e:
        logging.warning(f"[Calendar] API error: {e}")
        return _mock_calendar_events()


def analyze_calendar_pressure(events: list[dict]) -> dict:
    if not events:
        return {
            "total_meetings": 0, "total_meeting_hours": 0.0,
            "google_meet_count": 0, "back_to_back_count": 0,
            "longest_meeting_min": 0, "free_blocks": [],
            "meeting_density": 0.0, "early_meeting": False,
            "late_meeting": False, "pressure_level": "LOW",
        }

    timed = [e for e in events if not e.get("is_all_day", False) and e.get("duration_min", 0) > 0]
    total_meetings = len(timed)
    total_min = sum(e.get("duration_min", 0) for e in timed)
    total_hours = round(total_min / 60, 1)
    google_meet_count = sum(1 for e in timed if e.get("has_google_meet", False))
    longest = max((e.get("duration_min", 0) for e in timed), default=0)

    back_to_back = 0
    sorted_events = sorted(timed, key=lambda x: x.get("start", ""))
    for i in range(1, len(sorted_events)):
        try:
            prev_end = sorted_events[i - 1].get("end", "")
            curr_start = sorted_events[i].get("start", "")
            if prev_end and curr_start:
                pe = datetime.datetime.fromisoformat(prev_end.replace("Z", "+00:00"))
                cs = datetime.datetime.fromisoformat(curr_start.replace("Z", "+00:00"))
                gap_min = (cs - pe).total_seconds() / 60
                if gap_min < 15:
                    back_to_back += 1
        except Exception:
            pass

    early_meeting = False
    late_meeting = False
    for e in timed:
        try:
            start_str = e.get("start", "")
            end_str = e.get("end", "")
            if start_str:
                st = datetime.datetime.fromisoformat(start_str.replace("Z", "+00:00"))
                if st.hour < 9:
                    early_meeting = True
            if end_str:
                et = datetime.datetime.fromisoformat(end_str.replace("Z", "+00:00"))
                if et.hour >= 18:
                    late_meeting = True
        except Exception:
            pass

    workday_min = 9 * 60
    density = round((total_min / workday_min) * 100, 1) if workday_min > 0 else 0
    pressure_score = (total_meetings * 2 + back_to_back * 3 + (1 if early_meeting else 0) + (1 if late_meeting else 0) + google_meet_count * 1)

    if density > 70 or pressure_score >= 20: pressure_level = "CRITICAL"
    elif density > 50 or pressure_score >= 14: pressure_level = "HIGH"
    elif density > 30 or pressure_score >= 8: pressure_level = "MEDIUM"
    else: pressure_level = "LOW"

    return {
        "total_meetings": total_meetings, "total_meeting_hours": total_hours,
        "google_meet_count": google_meet_count, "back_to_back_count": back_to_back,
        "longest_meeting_min": longest, "free_blocks": [],
        "meeting_density": density, "early_meeting": early_meeting,
        "late_meeting": late_meeting, "pressure_level": pressure_level,
    }


